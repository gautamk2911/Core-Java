package javaDatatypes;

public class PrimitiveDatatypes {
	public static void main(String[] args) {
		int a=10;
		double b1 = 10;
		double b2 = 10.14523642;
		char c = 'c';
		float d = 10;
		boolean f = true;
		System.out.println("Integer: "+a+"\nDouble: "+b1+"\nMultidecimal Double: "+b2+"\nCharacter: "+c+"\nFloat: "+d+"\n"
				+ "boolean: "+f);
	}
	
}